#include "libft.h"

void	ft_lstaddend(t_list **alst, t_list *new)
{
	t_list *temp;

	temp = *alst;
	if (!alst || !new)
		return ;
	while (temp->next != NULL)
		temp = temp->next;
	temp->next = new;
}
