#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *temp;

	temp = dst;
	while (len && *src)
	{
		*temp = *src;
		src++;
		temp++;
		len--;
	}
	while (len)
	{
		*temp = 0;
		temp++;
		len--;
	}
	return (dst);
}
